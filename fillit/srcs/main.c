#include "ft.h"

int		main(int argc, char **argv)
{
	char	c;
	int		nb;

	c = '\0';
	nb = 0;
	if (argc == 2)
		ft_read(argv[1], c, nb);
	if (argc > 2 || argc < 2)
		ft_errors(-3);
	return (0);
}
