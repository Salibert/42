#include "ft.h"

void	ft_freetab(char ***tab)
{
	size_t i;

	i = 0;
	while ((*tab)[i])
	{
		ft_strdel(&(*tab)[i]);
		i++;
	}
	ft_strdel(&(*tab)[i]);
	*tab = NULL;
}
